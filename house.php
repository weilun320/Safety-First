safety features like the fire extinguishers, fire alarms, smoke alarms and heat detectors are needed. Family safety plans are an essential tool 
in fire safety because the plan helps family members to identify a fire escape plan. A safety plan includes knowing two ways out of every room, a 
safe place for members to meet outside of a burning home, and essential emergency telephone numbers. It is important to practice fire escape plans. 
