notifies the local fire department. Smoke alarms also known as smoke detectors, generally sound an audible and visual alarm. Smoke alarms are usually 
housed in a disk-shaped plastic enclosure about 6 inches in diameter and 1 inch thick and are often powered by a disposable battery. Heat detectors 
are a device that responds to changes in ambient temperature. Heat detectors are not meant to replace smoke detectors. They are often placed in rooms 
where standard smoke detectors are not suitable, such as laundry rooms, garages, and attics.
