    <h3 class="w3-text-blue-grey" style="text-align: center; font-size: 100px"><b>Road safety</b></h3><br>

<p><b>Road traffic safety</b> refers to the methods and measures used to prevent road users from being killed or seriously injured. Typical road users include: 
pedestrians, cyclists, motorists, vehicle passengers, horse-riders and passengers of on-road public transport (mainly buses and trams). The basic 
strategy of a Safe System approach is to ensure that in the event of a crash, the impact energies remain below the threshold likely to produce either 
death or serious injury. This threshold will vary from crash scenario to crash scenario, depending upon the level of protection offered to the road 
users involved. For example, the chances of survival for an unprotected pedestrian hit by a vehicle diminish rapidly at speeds greater than 30 km/h, 
whereas for a properly restrained motor vehicle occupant the critical impact speed is 50 km/h (for side impact crashes) and 70 km/h (for head-on crashes).<br><br>
The safety features in our cars contributed much to the road safety, three of the most important features that must be equipped are the airbags, 
Antilock Braking System(ABS) and seatbelts. Airbags are equipped in every vehicle in this era, the crash sensors connected to an onboard computer 
detect a frontal collision and trigger the bags. The bags inflate in a few milliseconds, a blink of an eye then immediately start deflating.<br><br>
Next, before antilock brakes came along, it was all too easy to lock up the wheels (stop them from turning) during hard braking. Sliding the 
front tires makes it impossible to steer, particularly on slippery surfaces. The ABS prevents this from happening by using sensors at each wheel 
and a computer that maximizes braking action at each individual wheel to prevent lock-up. ABS allows the driver to retain steering control while 
braking, so that the car can be maneuvered around an obstacle, if necessary.<br><br>
Seatbelt pretensioners instantly retract the belts to take up slack during a frontal impact. This also helps position occupants properly to 
take full advantage of a deploying air bag. Force limiters, a companion feature to pretensioners, manage the force that the shoulder belt builds 
up on the occupant's chest. After the pretensioners tighten it, force limiters let the belt play back out a little to reduce the force. Some models 
offer inflatable safety belts in the rear seat that further reduce the force of the belts themselves on rear passengers in an accident and spread 
those forces over a wider area, a particular concern with more fragile occupants, such as kids or the elderly.
</p>
