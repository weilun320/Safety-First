are important gear too, they prevent arm and wrist fractures, and knee guards can shield your knees from cuts and breaks.<br><br>
Don't rush into any sport or exercise without warming up first because if our muscles haven't been properly prepared tend to be injured easily. 
Start out with some light cardiovascular activities, such as easy jogging, jumping jacks, or brisk walking, just to get your muscles going. 
Follow your brief warm-up with some stretches. (Stretching works best after a warm-up because your tissues are more elastic [flexible] due 
to the increase in heat and blood flow to the muscles.)<br><br>In addition to warm-ups and stretches, practice sessions are also excellent 
preparation for most sports or activities. If you belong to a team, attend as many team practices and games as possible. This will put you in 
top physical condition and help you and your teammates work together, knowing how your teammates play will help prevent injuries.
