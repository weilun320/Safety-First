<p>Did you know that playing tennis with a badly strung racquet while wearing worn-out shoes can increase your risk of injury almost as much 
as playing football without shoulder pads? Using the wrong or not properly fitted with the suitable equipment is a major cause of injuries. 
The equipment you wear while participating in sports and other activities is key to preventing injuries. Helmets are important for sports such 
as football, hockey, baseball, softball, biking, skateboarding, inline skating, skiing, and snowboarding. The eye protection is also a must for 
many sports such as  soccer, basketball, racquet sports, snowboarding, street hockey, and baseball and softball when fielding. The most 
