<p><b>Internet safety</b> or online safety is trying to be safe on the internet and is the knowledge of maximizing the user's personal safety and 
security risks to private information and property associated with using the internet, and the self-protection from computer crime in general.<br><br>
As the number of internet users continues to grow worldwide, internets, governments and organizations have expressed concerns about the safety of 
children using the Internet. Safer Internet Day is celebrated worldwide in February to raise awareness about internet safety. In the UK the Get 
Safe Online campaign has received sponsorship from government agency Serious Organized Crime Agency (SOCA) and major Internet companies such as 
Microsoft and eBay.<br><br>Sensitive information such as personal information and identity, passwords are often associated with personal property 
(for example, bank accounts) and privacy and may present security concerns if leaked. Unauthorized access and usage of private information may 
result in consequence such as identity theft, as well as theft of property. Common causes of information security breaches include phishing, 
internet scams and malware.<br><br>Phishing is a type of scam where the scammers disguise as a trustworthy source in attempt to obtain private 
information such as passwords, and credit card information, etc. through the internet. Phishing often occurs through emails and instant messaging 
and may contain links to websites that direct the user to enter their private information. These fake websites are often designed to look identical 
to their legitimate counterparts to avoid suspicion from the user.<br><br>Internet scams are schemes that deceive the user in various ways in attempt 
to take advantage of them. Internet scams often aim to cheat the victim of personal property directly rather than personal information through false 
promises, confidence tricks and more. Malware, particularly spyware, is malicious software disguised as software designed to collect and transmit 
private information, such as passwords, without the user's consent or knowledge. They are often distributed through e-mail, software and files from 
unofficial locations. Malware is one of the most prevalent security concerns as often it is impossible to determine whether a file is infected, 
despite the source of the file.<br><br>Besides, the growth of the internet gave rise to many important services accessible to anyone with a connection. 
One of these important services is digital communication. While this service allowed us to communicate with others through the internet, this also 
allowed the communication with malicious users. While malicious users often use the internet for personal gain, this may not be limited to 
financial/material gain. This is especially a concern to parents and children, as children are often targets of these malicious users. Common threats 
to personal safety include: phishing, internet scams, malware, cyberstalking, cyberbullying, online predations and sextortion.<br><br>
Cyberstalking is the use of the Internet or other electronic means to stalk or harass an individual, a group of individuals, or an organization. 
It may include the making of false accusations or statements of fact (as in defamation), monitoring, making threats, identity theft, damage to data 
or equipment, the solicitation of minors for sex, or gathering information that may be used to harass. While for cyberbullying, it is the attack upon 
an individual or group through the use of electronic means such as instant messaging, social media, e-mail and other forms of online communication 
with the intent to abuse, intimidate, or overpower.<br><br>Online predation is the act of engaging an underage minor into inappropriate sexual 
relationships through the internet. Online predators may attempt to initiate and seduce minors into relationships through the use of chat rooms or 
internet forums. In a sample of 216 incarcerated sexual offenders, the behavior characteristics that emerged were categorized into three groups, 
the manipulative which is typically a child molester; the opportunist, typically a rapist and the coercive being a mixture of both rapists and 
child molesters.<br><br>Lastly, various websites on the internet might contain material that some deem offensive, distasteful or explicit, which may 
often be not of the user's liking. Such websites may include internet, shock sites, hate speech or otherwise inflammatory content. Such content may 
manifest in many ways, such as pop-up ads and unsuspecting links.</p>
